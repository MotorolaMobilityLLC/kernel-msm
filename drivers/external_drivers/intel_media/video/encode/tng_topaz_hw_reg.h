#define TOPAZ_MTX_WB_READ32(base, core, word, pointer) \
	do { \
		*(uint32_t *)(pointer) = *((uint32_t *)(base) + \
		(core)*MTX_WRITEBACK_DATASIZE_ROUND + (word)); \
	} while (0)

#define TOPAZ_MTX_WB_WRITE32(base, core, word, value) \
	do { \
		*((uint32_t *)(base) + \
		(core)*MTX_WRITEBACK_DATASIZE_ROUND + (word)) \
		= value; \
	} while (0)


